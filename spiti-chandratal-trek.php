  <script>
    // Banner Background Slider
    const section = document.getElementById("tripSection");
    const dotContainer = document.getElementById("dotPagination");
    const images = ["assets/img/spiti/spiti1.jpg"];
    let index = 0;
    let interval;

    images.forEach((_, i) => {
      const dot = document.createElement("span");
      dot.classList.add("dot");
      dot.style.opacity = "0";
      dot.addEventListener("click", () => { changeImage(i); resetInterval(); });
      dotContainer.appendChild(dot);
      setTimeout(() => { dot.style.transition = "opacity 0.3s ease"; dot.style.opacity = "1"; }, i * 100);
    });

    const dots = document.querySelectorAll(".dot");
    function changeImage(newIndex) {
      if (newIndex === index) return;
      section.classList.add("fade-in");
      const img = new Image();
      img.src = images[newIndex];
      img.onload = () => {
        requestAnimationFrame(() => {
          section.style.backgroundImage = `url('${images[newIndex]}')`;
          section.classList.remove("fade-in");
          index = newIndex;
          updateDots();
        });
      };
    }
    function updateDots() { dots.forEach((dot, i) => { dot.classList.toggle("active", i === index); }); }
    function nextImage() { const nextIndex = (index + 1) % images.length; changeImage(nextIndex); }
    function startInterval() { interval = setInterval(nextImage, 5000); }
    function resetInterval() { clearInterval(interval); startInterval(); }
    section.style.backgroundImage = `url('${images[index]}')`;
    updateDots();
    startInterval();
  </script>

